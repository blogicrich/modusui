<template>
  <v-app
    v-touch="{
    left: () => swipe('Left'),
    right: () => swipe('Right'),
    up: () => swipe('Up'),
    down: () => swipe('Down')
  }">
    <!-- HEADER -->
    <v-toolbar
      v-if="authenticated"
      color="white"
      app
      :clipped-left="clipped"
    >
      <img v-if="$vuetify.breakpoint.smAndDown" @click="drawerState = true" alt="" src="./assets/ed_logo.svg"><img>
      <img v-if="$vuetify.breakpoint.mdAndUp" alt="" src="./assets/ed_logo.svg"><img>
      <v-spacer></v-spacer>
      <v-layout row fill-height wrap justify-end>
        <v-icon outline medium class="mx-2" color="primary">person_outline</v-icon>
        <v-chip class="ml-1 mt-3 mb-3" v-for="(level, index) in level" :key="index" color="secondary" text-color="primary">{{ levelDisplay(level) }}</v-chip>
      </v-layout>
    </v-toolbar>
    <!-- SIDEBAR -->
    <transition-group name="navBtns">
      <!-- FOR MD AND UP -->
      <v-navigation-drawer
        v-if="level.find(level => level === 'CARER') && authenticated && $vuetify.breakpoint.mdAndUp"
        key="bp-lg"
        class="primary"
        mini-variant
        mini-variant-width="94"
        disable-route-watcher
        app
        clipped
        flat
      >
        <v-layout column fill-height align-center justify-space-between>
          <BaseAppNavBtn
            v-if="level.find(level => level === 'CARER') && level.find(level => level === 'CLIENT ADMINISTRATOR')"
            my-3
            right
            btnIcon="settings"
            btnColor="white"
            iconColor="white"
            route="landing"
            tip="Administration"
          />
          <BaseAppNavBtn
            my-3
            right
            v-for="item in items"
            :key="item.title"
            :btnIcon="item.btnIcon"
            :btnColor="item.btnColor"
            :iconColor="item.iconColor"
            :route="item.route"
            :tip="item.tip"
          />
        </v-layout>
      </v-navigation-drawer>
      <!-- FOR SM AND DOWN -->
      <v-navigation-drawer
        v-model="drawerState"
        v-if="level.find(level => level === 'CARER') && authenticated && $vuetify.breakpoint.smAndDown"
        key="bp-sm"
        class="primary"
        disable-route-watcher
        mini-variant
        mini-variant-width="160"
        temporary
        app
        clipped
        flat
      >
        <v-layout column fill-height align-start justify-start>
          <BaseAppNavBtn
            v-if="level.find(level => level === 'CARER') && level.find(level => level === 'CLIENT ADMINISTRATOR')"
            my-3
            right
            btnIcon="settings"
            btnColor="white"
            iconColor="white"
            route="landing"
            tip="Administration"
            title="Administration"
          />
          <BaseAppNavBtn
            my-3
            right
            v-for="item in items"
            :key="item.title"
            :btnIcon="item.btnIcon"
            :btnColor="item.btnColor"
            :iconColor="item.iconColor"
            :route="item.route"
            :tip="item.tip"
            :title="item.title"
          />
        </v-layout>
      </v-navigation-drawer>
    </transition-group>
    <!-- ROUTER VIEW -->
    <v-container class="px-0" fluid>
      <v-content>
        <v-slide-y-transition mode="out-in">
          <router-view class="px-0" @authenticated="setAuthenticated"/>
        </v-slide-y-transition>
      </v-content>
    </v-container>
    <!-- FOOTER -->
    <v-fade-transition>
      <v-footer
        v-if="
          level.find(level => level === 'SYSTEM ADMINISTRATOR') ||
          level.find(level => level === 'CLIENT ADMINISTRATOR') &&
          !(level.find(level => level === 'CARER')) &&
          this.authenticated"
        :fixed="fixed"
        color="white"
        app
      >
        <v-layout row fill height align-space-between justify-space-between>
          <v-layout class="pt-2" row align-center justify-start>
            <span class="ml-3" style="align-center">Version: ALPHA</span>
          </v-layout>
          <BaseAppNavBtn
            btnIcon="home"
            btnColor="primary"
            route="landing"
            tip="Home"
            top
          />
          <BaseAppNavBtn
            v-if="level.find(level => level === 'CARER') && this.authenticated"
            btnIcon="dashboard"
            btnColor="primary"
            route="dashboard"
            tip="Dashboard"
            top
          />
          <BaseAppNavBtn
            btnIcon="exit_to_app"
            btnColor="primary"
            route="/login"
            tip="Logout"
            top
          />
        </v-layout>
      </v-footer>
    </v-fade-transition>
    <!-- SNACKBAR -->
    <v-snackbar
      v-model="snackState"
      :color="snackColor"
      :timeout="snackTimeout"
      bottom
    >
      {{ snackText }}
      <v-btn
        dark
        flat
        @click="snackState = false"
      >
        Close
      </v-btn>
    </v-snackbar>
  </v-app>
</template>

<script>
/* eslint-disable */
import BaseAppNavBtn from '@/components/base/BaseAppNavFooterBtn.vue'
import { EventBus } from '@/mixins/eventBus.js'
import { setTimeout } from 'timers'
import { log, isArray } from 'util';

export default {
  name: 'App',
  components: {
    BaseAppNavBtn
  },
  data () {
    return {
      drawerState: false,
      swipeDirection: '',
      // Snackbar
      snackState: false,
      snackText: '',
      snackColor: '',
      snackTimeout: 0,
      // authenticated: {},
      appNavConfig: {footer: true, sideMenu: true, header: true},
      // navFooter: '',
      items: [
        {
          title: 'Dashboard',
          btnIcon: 'dashboard',
          btnColor: 'white',
          iconColor: 'white',
          route: 'dashboard',
          tip: 'Go to Dashboard'
        },
        {
          title: 'Consumption',
          btnIcon: 'date_range',
          btnColor: 'white',
          iconColor: 'white',
          route: 'consumptioncalendar',
          tip: 'View consumption'
        },
        {
          title: 'Add Drinks',
          btnIcon: 'local_drink',
          btnColor: 'white',
          iconColor: 'white',
          route: 'additionaldrinks',
          tip: 'Add Drinks'
        },
        {
          title: 'Away',
          btnIcon: 'calendar_today',
          btnColor: 'white',
          iconColor: 'white',
          route: 'away',
          tip: 'Record time away'
        },
        {
          title: 'Reports',
          btnIcon: 'report',
          btnColor: 'white',
          iconColor: 'white',
          route: 'reports',
          tip: 'Reports'
        },
        {
          title: 'Logout',
          btnIcon: 'exit_to_app',
          btnColor: 'white',
          iconColor: 'white',
          route: 'login',
          tip: 'Exit application'
        }
      ],
      clipped: true,
      fixed: false,
      // user: [],
    }
  },
  methods: {
    swipe (direction) {
      this.swipeDirection = direction
    },
    setAuthenticated (newStatus) {
      if (this.level) {
        if (this.level.find(level => level === 'SYSTEM ADMINISTRATOR') || this.level.find(level => level === 'CLIENT ADMINISTRATOR')) {
          this.$router.push('/landing')
        }
        if (this.level.find(level => level === 'CARER') || 
        (this.level.find(level => level === 'CARER') && this.level.find(level => level === 'CLIENT ADMINISTRATOR'))) {
          this.$router.push('/dashboard')
        }
      } else {
        this.$store.dispatch('LOGOUT')
        this.$router.push('/login')
      }
    },
    showSnack (eventPayload) {
      this.snackText = eventPayload.text
      this.snackColor = eventPayload.color
      this.snackTimeout = eventPayload.time
      this.snackState = eventPayload.state
    },
    logout () {
      this.$store.dispatch('LOGOUT')
    },
    home () {
      this.$router.push('/landing')
    },
    levelDisplay (level) {
      if (this.$vuetify.breakpoint.mdAndUp) {
        return level
      } else if (this.$vuetify.breakpoint.smAndDown) {
        if(level.includes('CARER')) {
          return 'CARER'
        } else if (level.includes('CLIENT ADMINISTRATOR')) {
          return 'CLI ADMIN'
        } else if (level.includes('SYSTEM ADMINISTRATOR')) {
          return 'SYS ADMIN'
        }
      }
    },
  },
  computed: {
    userLevel () {
      const greeting = 'LOGGED IN AS: '
      let concatUser = ''
      for (let i = 0; i < this.level; i++) {
        const element = String(this.level[i])
        concatUser = concatUser + element + ' '
      }
      return greeting + concatUser
    },
    authenticated: function () {
      return this.$store.getters.authenticated
    },
    level: function () {
      return this.$store.getters.level
    },
    loadStatus: function () {
      return this.$store.getters.authDataLoading
    },
    isActive: function () {
      return this.$store.getters.isActive
    }
  },
  watch: {
    swipeDirection: function () {
      if (this.swipeDirection === 'Right') {
        this.drawerState = true
        this.swipeDirection = ''
      }
    },
    authenticated: function () {
      if (this.authenticated === null || this.authenticated === false || this.authenticated === undefined) {
        this.logout()
        this.$router.push('/login')
      } else {
        console.log('Still Authenticated: ', this.authenticated)
      }
    },
  },
  mounted () {
    if (this.authenticated === null || this.authenticated === undefined || this.authenticated === false) {
      this.logout()
    }
    EventBus.$on('snack-msg', data => {
      this.showSnack(data)
    })
  },
  destroyed () {
    EventBus.$off('snack-msg')
  }
}
</script>
