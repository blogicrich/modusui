<template>
  <v-app>
    <v-toolbar
      v-if="authenticated.state"
      color="white"
      app
      :clipped-left="clipped"
    >
    <v-toolbar-side-icon v-if="$vuetify.breakpoint.md" @click.stop="activeDrawer = !activeDrawer"></v-toolbar-side-icon>
      <v-toolbar-title v-text="title"></v-toolbar-title>
      <img alt="" src="./assets/ed_logo.svg"><img>
      <v-spacer></v-spacer>
      <v-badge v-if="(authenticated.level === 'CARER')" left overlap color="error">
        <span slot="badge" class="text-badge">{{ alerts }}</span>
        <v-icon
          medium
          color="primary"
          @click="$router.push('/login')"
        >
          notifications
        </v-icon>
      </v-badge>
      <v-icon outline class="mx-2" color="primary">person_outline</v-icon>
      <span v-if="authenticated.state && $vuetify.breakpoint.smAndUp">Logged in as: {{ user }}</span>
    </v-toolbar>
    <v-fade-transition>
      <v-navigation-drawer
        v-model="activeDrawer"
        v-if="authenticated.level === 'CARER' && authenticated.state && $vuetify.breakpoint.lgAndUp"
        class="primary"
        mini-variant
        disable-route-watcher
        app
        clipped
        flat
      >
        <v-layout column fill-height align-center justify-space-between>
          <BaseAppNavBtn
            my-3
            right
            v-for="item in items"
            :key="item.title"
            :btnIcon="item.btnIcon"
            :btnColor="item.btnColor"
            :iconColor="item.iconColor"
            :route="item.route"
            :tip="item.tip"
            @navBtnClicked="check"
          />
        </v-layout>
      </v-navigation-drawer>
      <v-navigation-drawer
        v-model="activeDrawer"
        v-if="authenticated.level === 'CARER' && authenticated.state && $vuetify.breakpoint.mdAndDown"
        class="primary"
        disable-route-watcher
        mini-variant
        mini-variant-width="160"
        app
        clipped
        flat
      >
        <v-layout v-if="authenticated.level && $vuetify.breakpoint.mdAndDown" column fill-height align-start justify-start>
          <BaseAppNavBtn
            my-3
            right
            v-for="item in items"
            :key="item.title"
            :btnIcon="item.btnIcon"
            :btnColor="item.btnColor"
            :iconColor="item.iconColor"
            :route="item.route"
            :tip="item.tip"
            :title="item.title"
            @navBtnClicked="check"
          />
        </v-layout>
      </v-navigation-drawer>
    </v-fade-transition>
    <v-container fluid>
      <v-content>
        <v-slide-y-transition mode="out-in">
          <router-view class="px-0" @authenticated="setAuthenticated"/>
        </v-slide-y-transition>
      </v-content>
    </v-container>
    <v-fade-transition>
      <v-footer
        v-if="authenticated.state && authenticated.level !== 'CARER' && $vuetify.breakpoint.smAndUp"
        :class="(authenticated.level === 'CARER') ? 'hide' : 'elevation-5 pa-4' "
        :fixed="fixed"
        color="white"
        app
      >
        <v-layout row fill height align-space-between justify-space-between>
          <v-layout class="pt-2" row align-center justify-start>
            <span style="align-center">Version: ALPHA</span>
          </v-layout>
          <BaseAppNavBtn
            btnIcon="home"
            btnColor="primary"
            route="landing"
            tip="Home"
          />
          <BaseAppNavBtn
            v-if="(authenticated.level === 'CLIENT ADMINISTRATOR')"
            btnIcon="dashboard"
            btnColor="primary"
            route="landing"
            tip="Dashboard"
          />
          <BaseAppNavBtn
            btnIcon="exit_to_app"
            btnColor="primary"
            route="login"
            tip="Logout"
          />
        </v-layout>
      </v-footer>
    </v-fade-transition>
  </v-app>
</template>

<script>
import BaseAppNavBtn from '@/components/base/BaseAppNavFooterBtn.vue'

export default {
  name: 'App',
  components: {
    BaseAppNavBtn
  },
  data () {
    return {
      activeDrawer: true,
      alerts: 0,
      authenticated: {
        state: false,
        level: null
      },
      items: [
        {
          title: 'Dashboard',
          btnIcon: 'dashboard',
          btnColor: 'white',
          iconColor: 'white',
          route: 'dashboard',
          tip: 'Go to Dashboard'
        },
        {
          title: 'Add Drinks',
          btnIcon: 'local_drink',
          btnColor: 'white',
          iconColor: 'white',
          route: 'additionaldrinks',
          tip: 'Add Drinks'
        },
        {
          title: 'Alerts',
          btnIcon: 'notification_important',
          btnColor: 'white',
          iconColor: 'white',
          route: 'alerts',
          tip: 'View Alerts'
        },
        {
          title: 'Away',
          btnIcon: 'calendar_today',
          btnColor: 'white',
          iconColor: 'white',
          route: 'away',
          tip: 'Record time away'
        },
        {
          title: 'Reports',
          btnIcon: 'report',
          btnColor: 'white',
          iconColor: 'white',
          route: 'reports',
          tip: 'Reports'
        },
        {
          title: 'Logout',
          btnIcon: 'exit_to_app',
          btnColor: 'white',
          iconColor: 'white',
          route: '',
          tip: 'Exit application'
        }
      ],
      clipped: true,
      fixed: false,
      title: '',
      user: 'User'
    }
  },
  methods: {
    setAuthenticated (newStatus) {
      this.authenticated.state = newStatus.state
      this.authenticated.level = newStatus.level
      localStorage.auth = JSON.stringify(this.authenticated)
      this.user = newStatus.level
      // console.log(JSON.parse(localStorage.auth))
      if (newStatus.level === 'SYSTEM ADMINISTRATOR' || newStatus.level === 'CLIENT ADMINISTRATOR') {
        this.$router.push('/landing')
      } else if (newStatus.level === 'CARER') {
        this.$router.push('/dashboard')
      }
    },
    check (item) {
      console.log(item);
      if (item.route === 'logout') logout()
    },
    logout () {
      this.authenticated = { state: false, level: null }
      localStorage.auth = JSON.stringify(this.authenticated)
      this.activeDrawer = false
      this.user = ''
      this.$router.push('/login')
    },
    home () {
      this.$router.push('/landing')
    }
  },
  watch: {
    authenticated: function () {
      console.log('Auth changed: ', this.authenticated)
    }
  },
  mounted () {
    if (!this.authenticated.state) {
      this.$router.replace('/login')
    }
  }
}
</script>
