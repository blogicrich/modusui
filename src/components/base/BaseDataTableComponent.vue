<template>
  <div>
  <!-- Table Header: Title and '+' button -->
    <v-toolbar class="pa-1 my-1 elevation-1" flat color="white">
      <h2 class="table-header">{{ tableTitle }}</h2>
      <v-divider
        class="mx-2"
        inset
        vertical
      ></v-divider>
      <v-spacer></v-spacer>
  <!-- Edit or New item: Title and '+' button -->
      <v-dialog v-model="dialog" max-width="500px">
        <v-btn slot="activator" color="green" fab small dark class="ma-1">
          <v-icon dark>add</v-icon>
        </v-btn>
        <v-card>
          <v-card-title>
            <span class="table-header">{{ dialogTitle }}</span>
          </v-card-title>
          <v-card-text>
            <v-container>
              <v-layout row wrap justify-space-around>
                <v-flex v-for="(item, key) in newItem" :key="item.name" xs12 md6>
                  <v-text-field
                    v-if="item.cellType === 'tb'"
                    class="ma-1"
                    :label="item.cellLabel"
                    v-model="newItem[key].sync"
                    color="primary"
                    outline
                  ></v-text-field>
                  <v-select
                    v-else-if="item.cellType === 'md'"
                    class="ma-1"
                    v-model="newItem[key].sync"
                    :items="menuItems"
                    :label="newItem[key].cellLabel"
                    color="primary"
                    outline
                  ></v-select>
                </v-flex>
              </v-layout>
            </v-container>
          </v-card-text>
          <v-card-actions>
            <v-spacer></v-spacer>
            <v-btn color="primary darken-1" flat @click.native="close">Cancel</v-btn>
            <v-btn color="primary darken-1" flat @click.native="save">Save</v-btn>
          </v-card-actions>
        </v-card>
      </v-dialog>
    </v-toolbar>
  <!-- New item: Search bar -->
    <v-card>
      <v-fade-transition>
        <v-card-title v-if="!searchBarHidden">
          <v-spacer></v-spacer>
          <v-text-field
            v-model="search"
            append-icon="search"
            :label="searchLabel"
            single-line
            hide-details
          ></v-text-field>
          <v-btn class="mx-3" fab dark small color="error" @click="search = null">
            <v-icon dark>close</v-icon>
          </v-btn>
          <v-spacer></v-spacer>
        </v-card-title>
      </v-fade-transition>
    </v-card>
  <!-- Data table -->
    <v-data-table
      :headers="headers"
      :items="items"
      :search="search"
      v-model="selected"
      :item-key="itemKey"
      select-all
      hide-actions
      :pagination.sync="pagination"
      :total.items="this.items.length"
      class="elevation-1"
    >
  <!-- Table: Headers -->
    <template slot="headerCell" slot-scope="props">
      <v-tooltip bottom>
        <span slot="activator">
          {{ props.header.text }}
        </span>
        <span>
          {{ props.header.text }}
        </span>
      </v-tooltip>
    </template>
  <!-- Table: Row data-->
    <template slot="items" slot-scope="props">
      <td>
        <v-checkbox
          v-model="props.selected"
          primary
        ></v-checkbox>
      </td>
       <td
         class="text-xs-left"
         v-for="header in headers"
         :key="header.text"
       >
         <v-edit-dialog
            :return-value.sync="props.item[header.value]"
            lazy
            large
            permanent
            persistent
            @save="msgSave"
            @cancel="msgCancel"
            @close="msgClose"
          >
          <div>{{ props.item[header.value] }}</div>
          <div slot="input" class="my-3 title">{{ header.text }}</div>
            <v-text-field
              v-if="header.cellType == 'tb'"
              slot="input"
              v-model="props.item[header.value]"
              label="Edit"
              single-line
              counter
            ></v-text-field>
            <v-select
              v-else-if="header.cellType === 'md'"
              slot="input"
              class="ma-1"
              v-model="props.item[header.value]"
              :items="menuItems"
              :label="header.text"
              color="primary"
              large
              outline
            ></v-select>
        </v-edit-dialog>
      </td>
      </template>
    </v-data-table>
  <!-- Table: Footer Pagination CRUD function buttons-->
    <v-card>
      <v-container class="my-1">
        <v-layout row wrap>
          <!-- <v-layout xs2 md2> -->
            <v-flex xs1 md1>
              <v-select
                class="ma-1"
                v-model="pagination.rowsPerPage"
                :items="rows"
                label="rows"
                color="primary"
              ></v-select>
            </v-flex>
          <!-- </v-layout> -->
          <v-flex>
            <v-layout align-end justify-end>
              <v-btn fab dark small color="primary" @click="searchDisplay">
                <v-icon dark>search</v-icon>
              </v-btn>
              <v-btn fab dark small color="error" @click="deleteItem">
                <v-icon dark>delete</v-icon>
              </v-btn>
            </v-layout>
          </v-flex>
          <v-layout row align-start justify-start>
            <v-pagination
              class="ma-1"
              v-model="pagination.page"
              :length="pages"
              >
            </v-pagination>
          </v-layout>
        </v-layout>
      </v-container>
    </v-card>
    <!-- Table: Footer Pagination CRUD function buttons-->
    <v-snackbar v-model="snack" :timeout="3000" :color="snackColor">
      {{ snackText }}
      <v-btn flat @click="snack = false">Close</v-btn>
    </v-snackbar>
  </div>
</template>

<script>

export default {
  name: 'BaseDataTable',
  data () {
    return {
      rows: [ 5, 10, 15, 20, 25, 50, 100 ],
      dialog: false,
      search: '',
      selected: [],
      searchBarHidden: true,
      pagination: {},
      snackColor: 'primary',
      snackText: '',
      rowDisplayNo: 5,
      snack: false
    }
  },
  props: {
    items: Array,
    headers: Array,
    menuItems: Array,
    btnTitle: String,
    dialogTitle: String,
    tableTitle: String,
    newItem: Array,
    itemKey: String,
    searchLabel: String,
  },
  computed: {
    pages () {
      if (this.pagination.rowsPerPage == null || this.pagination.totalItems == null) {
        return 0
      } else {
        return Math.ceil(this.pagination.totalItems / this.pagination.rowsPerPage)
      }
    }
  },
  methods: {
    deleteItem () {
      this.$emit('deleteSelected', this.selected)
      this.selected = []
    },
    close () {
      this.dialog = false
      console.log('Dialog Closing')
    },
    searchDisplay () {
      this.searchBarHidden = !this.searchBarHidden
      this.search = null
      return this.searchBarHidden
    },
    save () {
      console.log('Fired')
      this.$emit('newItem', this.newItem)
      this.close()
    },
    msgSave () {
      this.snack = true
      this.snackColor = 'success'
      this.snackText = 'Data saved'
    },
    msgCancel () {
      this.snack = true
      this.snackColor = 'error'
      this.snackText = 'Canceled'
    },
    msgOpen () {
      this.snack = true
      this.snackColor = 'info'
      this.snackText = 'Dialog opened'
    },
    msgClose () {
      console.log('Snackbar closed')
    }

  },
  mounted () {
    console.log(this.items)
  }
}
</script>

