<template>
  <div>
  <!-- Table Header: Title and '+' button -->
    <v-toolbar class="pa-1 my-1 elevation-1" flat color="white">
      <h2 class="table-header">{{ tableTitle }}</h2>
      <v-divider
        class="mx-2"
        inset
        vertical
      ></v-divider>
      <v-spacer></v-spacer>
  <!-- Edit or New item: Title and '+' button -->
      <v-dialog v-model="dialog" max-width="500px">
        <v-btn slot="activator" color="green" fab small dark class="ma-1">
          <v-icon dark>add</v-icon>
        </v-btn>
        <v-card>
          <v-card-title>
            <span class="table-header">{{ dialogTitle }}</span>
          </v-card-title>
          <v-card-text>
            <v-container>
              <v-layout row wrap justify-space-around>
                <v-flex v-for="(item, key) in newItem" :key="item.name" xs12 md4>
                  <v-text-field v-if="item.cellType === 'tb'" class="mx-1" :label="item.cellLabel" v-model="newItem[key].sync"></v-text-field>
                  <v-overflow-btn v-else-if="item.cellType === 'dd'" class="mx-1" :label="item.cellLabel" :items="headers" v-model="newItem[key].sync"></v-overflow-btn>
                </v-flex>
              </v-layout>
            </v-container>
          </v-card-text>
          <v-card-actions>
            <v-spacer></v-spacer>
            <v-btn color="primary darken-1" flat @click.native="close">Cancel</v-btn>
            <v-btn color="primary darken-1" flat @click.native="save">Save</v-btn>
          </v-card-actions>
        </v-card>
      </v-dialog>
    </v-toolbar>
  <!-- New item: Search bar -->
    <v-card>
      <v-fade-transition>
        <v-card-title v-if="!searchBarHidden">
          <v-spacer></v-spacer>
          <v-text-field
            v-model="search"
            append-icon="search"
            :label="searchLabel"
            single-line
            hide-details
          ></v-text-field>
          <v-spacer></v-spacer>
        </v-card-title>
      </v-fade-transition>
    </v-card>
    <!-- Data table -->
    <v-data-table
      :headers="headers"
      :items="items"
      :search="search"
      v-model="selected"
      :item-key="itemKey"
      select-all
      hide-actions
      :pagination.sync="pagination"
      :total.items="pagination.totalItems"
      class="elevation-1"
    >
    <!-- Table: Headers -->
    <template slot="headerCell" slot-scope="props">
      <v-tooltip bottom>
        <span slot="activator">
          {{ props.header.text }}
        </span>
        <span>
          {{ props.header.text }}
        </span>
      </v-tooltip>
    </template>
    <!-- Table: Row data-->
    <template slot="items" slot-scope="props">
      <td>
        <v-checkbox
          v-model="props.selected"
          primary
        ></v-checkbox>
      </td>
      <td class="text-xs-left" v-for="header in headers" :key="header.text">{{ props.item[header.value] }}</td>
      </template>
    </v-data-table>
    <!-- Table: Footer Pagination CRUD function buttons-->
    <v-card>
      <v-container class="my-1">
        <v-layout row>
          <v-flex>
            <v-layout align-end justify-end>
              <!-- <v-btn fab dark small color="green">
                <v-icon dark>edit</v-icon>
              </v-btn> -->
              <v-btn fab dark small color="primary" @click="searchDisplay">
                <v-icon dark>search</v-icon>
              </v-btn>
              <v-btn fab dark small color="error" @click="deleteItem">
                <v-icon dark>delete</v-icon>
              </v-btn>
            </v-layout>
          </v-flex>
          <v-layout row align-start justify-start>
            <v-pagination class="ma-2" v-model="pagination.page" :length="pages"></v-pagination>
          </v-layout>
        </v-layout>
      </v-container>
    </v-card>
  </div>
</template>

<script>

export default {
  name: 'BaseDataTable',
  data () {
    return {
      dialog: false,
      search: '',
      selected: [],
      searchBarHidden: true,
      pagination: {}
    }
  },
  props: {
    items: Array,
    headers: Array,
    btnTitle: String,
    dialogTitle: String,
    tableTitle: String,
    newItem: Array,
    itemKey: String,
    searchLabel: String
  },
  computed: {
    pages () {
      if (this.pagination.rowsPerPage == null ||
        this.pagination.totalItems == null
      ) return 0
      return Math.ceil(this.pagination.totalItems / this.pagination.rowsPerPage)
    }
  },
  methods: {
    deleteItem () {
      this.$emit('deleteItem', this.selected)
    },

    close () {
      this.dialog = false
      console.log('Dialog Closing')
    },
    searchDisplay () {
      this.searchBarHidden = !this.searchBarHidden
      return this.searchBarHidden
    },
    save () {
      console.log("Fired")
      this.$emit('newItem', this.newItem)
      this.close()
    }
  },
  mounted () {
    console.log(this.items)
  }
}
</script>

